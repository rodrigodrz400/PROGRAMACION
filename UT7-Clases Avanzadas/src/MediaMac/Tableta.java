package MediaMac;

public class Tableta extends Aparato {

	//Debe tener además como atributo las pulgadas de pantalla y su peso en gramos.
	private double pulgadas;
	private double peso;
	
	public Tableta(String numProducto, String numSerie, String nombre, String so) {
		super(numProducto, numSerie, nombre, so);
		this.pulgadas=pulgadas;
		this.peso=peso;
	}
	
}
